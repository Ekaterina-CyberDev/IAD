# Создаем двухуровневый список с разными типами чисел
mixed_list = [
    [1, 2.5, 3+4j],           # Первый внутренний список: целое число 1, вещественное число 2.5, комплексное число 3+4j
    
    [4, 5.7, 6-2j, 7+0j],     # Второй внутренний список: целое 4, вещественное 5.7, комплексное 6-2j, комплексное 7+0j
    
    [8.9, 9+1j, 10],          # Третий внутренний список: вещественное 8.9, комплексное 9+1j, целое 10
    
    [11+3j, 12.4, 13+7j]      # Четвертый внутренний список: комплексное 11+3j, вещественное 12.4, комплексное 13+7j
]

# Создаем пустой список, в который будем добавлять найденные комплексные числа
complex_numbers = []

# Начинаем внешний цикл для перебора каждого подсписка в основном списке
for sublist in mixed_list:          # Перебираем каждый внутренний список (sublist) в mixed_list
    # Начинаем внутренний цикл для перебора каждого элемента в текущем подсписке
    for item in sublist:            # Перебираем каждый элемент (item) в текущем подсписке
        # Проверяем, является ли текущий элемент комплексным числом
        if isinstance(item, complex):  # Функция isinstance проверяет тип объекта
            # Если элемент комплексный, добавляем его в список complex_numbers
            complex_numbers.append(item)

# Преобразуем список complex_numbers в кортеж (неизменяемую коллекцию)
complex_tuple = tuple(complex_numbers)  # Функция tuple() преобразует список в кортеж

# Выводим исходный список для наглядности
print("Исходный двухуровневый список:")
print(mixed_list)  # Выводим весь созданный список

# Выводим пустую строку для разделения вывода
print("\nКомплексные числа в кортеже:")  # \n - символ переноса строки

# Выводим полученный кортеж с комплексными числами
print(complex_tuple)  # Кортеж выводится в круглых скобках