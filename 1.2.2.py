# Создаем список для хранения частичных сумм ряда Фибоначчи
fibonacci_sums = []

# Первые два числа Фибоначчи
a, b = 0, 1
partial_sum = 0  # Переменная для накопления суммы

# Генерируем 100 первых элементов последовательности частичных сумм
for i in range(100):
    partial_sum += a  # Добавляем текущее число Фибоначчи к сумме
    fibonacci_sums.append(partial_sum)  # Добавляем частичную сумму в список
    
    # Обновляем числа Фибоначчи для следующей итерации
    a, b = b, a + b

# Преобразуем список в множество (убираем возможные дубликаты, хотя их не будет)
fibonacci_set = set(fibonacci_sums)

# Запрашиваем у пользователя номер элемента
try:
    index = int(input("Введите номер элемента (от 0 до 99): "))
    
    # Проверяем, что номер в допустимом диапазоне
    if 0 <= index < 100:
        # Получаем элемент по указанному номеру
        element = fibonacci_sums[index]
        
        # Преобразуем элемент в строку, чтобы посчитать цифры
        element_str = str(element)
        
        # Считаем только значащие цифры (игнорируем точку и минус если есть)
        significant_digits = 0
        for char in element_str:
            if char.isdigit():  # Проверяем, является ли символ цифрой
                significant_digits += 1  # Увеличиваем счетчик цифр
        
        # Выводим результаты
        print(f"\nЭлемент с номером {index}: {element}")
        print(f"Число значащих цифр: {significant_digits}")
    else:
        print("Ошибка! Номер должен быть от 0 до 99")

except ValueError:
    print("Ошибка! Введите целое число")