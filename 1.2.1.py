# Задача 1.2.1 - Комплексные числа в кортеж

# Создаем двухуровневый список с разными типами чисел
mixed_list = [
    [1, 2.5, 3+4j],           # Первый подсписок: целое, вещественное, комплексное
    [4, 5.7, 6-2j, 7+0j],     # Второй подсписок
    [8.9, 9+1j, 10],          # Третий подсписок
    [11+3j, 12.4, 13+7j]      # Четвертый подсписок
]

# Создаем пустой список для комплексных чисел
complex_numbers = []

# Проходим по всем элементам двухуровневого списка
for sublist in mixed_list:          # Перебираем каждый подсписок
    for item in sublist:            # Перебираем каждый элемент в подсписке
        if isinstance(item, complex):  # Проверяем, является ли элемент комплексным числом
            complex_numbers.append(item)  # Если да, добавляем в список

# Преобразуем список комплексных чисел в кортеж
complex_tuple = tuple(complex_numbers)

# Выводим результаты
print("Исходный двухуровневый список:")
print(mixed_list)
print("\nКомплексные числа в кортеже:")
print(complex_tuple)