# Определяем функцию для чтения числа от пользователя
def read_number():
    # Создаем бесконечный цикл для повторения ввода до получения правильного числа
    while True:
        # Запрашиваем ввод числа у пользователя
        num = input("Введите 10-значное число: ")
        # Проверяем: длина строки равна 10 И все символы - цифры
        if len(num) == 10 and num.isdigit(): 
            # Если условие выполняется, возвращаем число и выходим из функции
            return num
        # Если условие не выполнилось, выводим сообщение об ошибке
        print("Ошибка! 10 цифр!")

# Определяем функцию для вычисления НОД числа 8 и введенного числа
def gcd_eight(num):
    # Получаем последнюю цифру числа (последний символ строки)
    last_digit = num[-1]
    # Проверяем, является ли последняя цифра нечетной (1,3,5,7,9)
    if last_digit in '13579': 
        # Если число нечетное, НОД с 8 равен 1
        return '1'
    
    # Берем последние три цифры числа и преобразуем в целое число
    last_three = int(num[-3:])
    # Проверяем, делится ли число из последних трех цифр на 8 без остатка
    if last_three % 8 == 0: 
        # Если делится на 8, то НОД равен 8
        return '8'
    
    # Берем последние две цифры числа и преобразуем в целое число
    last_two = int(num[-2:])
    # Проверяем: если последние две цифры делятся на 4, возвращаем 4, иначе 2
    return '4' if last_two % 4 == 0 else '2'

# Вызываем функцию для ввода числа и сохраняем результат в переменную
number = read_number()
# Выводим результат: НОД числа 8 и введенного числа
print(f"НОД(8, {number}) = {gcd_eight(number)}")